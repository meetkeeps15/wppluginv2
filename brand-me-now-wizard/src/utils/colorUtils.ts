export const parseColors = (input: string): string[] | null => {
  const colors = input.split(",").map(c => c.trim().toLowerCase()).filter(c => c);
  if (colors.length === 0 || colors.length > 3) return null;
  const hexes = colors.map(c => colorMap[c]);
  if (hexes.some(h => !h)) return null;
  return hexes;
};
